// Helper functions
export function getProductsByTier(tier: string): AnbernicProduct[] {
  return products.filter((p) => p.performanceTier === tier)
}

export function getProductsByFormFactor(formFactor: string): AnbernicProduct[] {
  return products.filter((p) => p.formFactor === formFactor)
}

export function getProductsByOS(os: string): AnbernicProduct[] {
  return products.filter((p) => p.os === os)
}

export function getProductsByPriceRange(min: number, max: number): AnbernicProduct[] {
  return products.filter((p) => p.price >= min && p.price <= max)
}

export function searchProducts(query: string): AnbernicProduct[] {
  const q = query.toLowerCase()
  return products.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.fullName.toLowerCase().includes(q) ||
      p.features.some((f) => f.toLowerCase().includes(q)) ||
      p.bestFor.some((b) => b.toLowerCase().includes(q)),
  )
}

export function getRelatedProducts(productId: string, limit = 4): AnbernicProduct[] {
  const product = productsById[productId]
  if (!product) return []

  // Find products with similar characteristics
  return products
    .filter((p) => p.id !== productId)
    .map((p) => ({
      product: p,
      score:
        (p.performanceTier === product.performanceTier ? 3 : 0) +
        (p.formFactor === product.formFactor ? 2 : 0) +
        (p.os === product.os ? 1 : 0) +
        (Math.abs(p.price - product.price) < 30 ? 1 : 0),
    }))
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map((item) => item.product)
}

export function calculateValueScore(product: AnbernicProduct): number {
  // Performance per dollar (normalized)
  const performanceScore = product.processor.performanceScore
  const price = product.price
  return Math.round((performanceScore / price) * 100)
}
